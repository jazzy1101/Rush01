int	test_complete(int tab[6][6])
{
	int	y;
	int	x;

	y = 1;
	x = 1;
	while (y < 5)
	{
		while (x < 5)
		{
			if (tab[y][x] == 0)
				return (1);
			x++;
		}
		x = 1;
		y++;
	}
	return (0);
}
